"use strict";

const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const checkOnly = process.argv.includes("--check");

function read(file) {
  return fs.readFileSync(path.join(root, file), "utf8");
}

function write(file, value) {
  fs.writeFileSync(path.join(root, file), value, "utf8");
}

function readJson(file) {
  return JSON.parse(read(file));
}

function replaceSection(content, name, body) {
  const start = `<!-- RIGSCOPE:${name}:start -->`;
  const end = `<!-- RIGSCOPE:${name}:end -->`;
  const pattern = new RegExp(`${escapeRegExp(start)}[\\s\\S]*?${escapeRegExp(end)}`);
  if (!pattern.test(content)) {
    throw new Error(`Missing generated section markers for ${name}.`);
  }
  return content.replace(pattern, `${start}\n${body.trim()}\n${end}`);
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function normalizeNewlines(value) {
  return value.replace(/\r\n/g, "\n");
}

function renderBullets(items) {
  return items.map((item) => `- ${item}`).join("\n");
}

function renderParagraphs(items) {
  return items.join("\n\n");
}

function buildDemoFixtures(pkg, manifest) {
  return {
    generatedBy: "scripts/sync-docs-demo.js",
    app: {
      name: manifest.app.name,
      version: pkg.version,
      liveDemo: manifest.app.liveDemo,
      scoreboardUrl: manifest.app.scoreboardUrl
    },
    ...manifest.demo
  };
}

function renderDemoFixtures(fixtures) {
  return [
    "/* This file is generated by npm run sync:docs. Edit docs/product-manifest.json instead. */",
    `window.RIGSCOPE_DEMO_FIXTURES = ${JSON.stringify(fixtures, null, 2)};`,
    ""
  ].join("\n");
}

function buildReadme(pkg, manifest) {
  if (manifest.app.version !== pkg.version) {
    throw new Error(`docs/product-manifest.json version ${manifest.app.version} does not match package.json ${pkg.version}.`);
  }

  let readme = normalizeNewlines(read("README.md"));
  readme = replaceSection(readme, "CURRENT_HIGHLIGHTS", renderBullets(manifest.readme.currentHighlights));
  readme = replaceSection(readme, "DEMO_NOTE", renderParagraphs(manifest.readme.demoNote));
  return readme;
}

function compareOrWrite(file, next) {
  const current = fs.existsSync(path.join(root, file)) ? normalizeNewlines(read(file)) : "";
  const normalizedNext = normalizeNewlines(next);
  if (current === normalizedNext) return false;
  if (checkOnly) {
    throw new Error(`${file} is out of sync. Run npm run sync:docs.`);
  }
  write(file, normalizedNext);
  return true;
}

function main() {
  const pkg = readJson("package.json");
  const manifest = readJson("docs/product-manifest.json");
  const readme = buildReadme(pkg, manifest);
  const fixtures = buildDemoFixtures(pkg, manifest);
  const demoFixtures = renderDemoFixtures(fixtures);

  const changed = [
    compareOrWrite("README.md", readme),
    compareOrWrite("public/demo-fixtures.js", demoFixtures)
  ].some(Boolean);

  if (checkOnly) {
    console.log("Docs and demo fixtures are in sync.");
  } else {
    console.log(changed ? "Synced README and demo fixtures." : "README and demo fixtures already in sync.");
  }
}

try {
  main();
} catch (error) {
  console.error(error.message);
  process.exit(1);
}
